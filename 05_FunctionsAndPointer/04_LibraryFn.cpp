#include <iostream>
#include <cmath>
using namespace std;

int main(){

    cout << "Sqrt(4) : " << sqrt(4) << endl;
    cout << "Min(4,81) : " << min(4, 81) << endl;
    cout << "Cbrt(1000) : " << cbrt(1000) << endl;
    cout << "Max(4, 81) : " << max(4, 81) << endl;
    cout << "Pow(3, 4) : " << pow(3,4) << endl;
    cout << "Round(2.82) : " << round(2.82) << endl;

    return 0;
}